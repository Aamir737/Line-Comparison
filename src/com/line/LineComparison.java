package com.line;

public class LineComparison {

	public static void main(String[] args) {
		System.out.println("Welcome to line comparison computation program");
		int x1,x2,y1,y2;
		x1=5;
		x2=10;
		y1=8;
		y2=10;
		double len1=Math.sqrt((x2-x1)*(x2-x1)+(y2-y1)*(y2-y1));
		System.out.println(len1);

	}

}
